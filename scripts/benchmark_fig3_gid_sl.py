#!/usr/bin/env python3
"""Compare the Fig. 3 d'Alembert strain field with Stepanov GID_sl.

The checked-in strain profile was generated by strain-wave-simulation using
the paper_fig3_gaas preset and ttm_dalembert_cr_gaas at 1.8 ns.  GID_sl was
queried with one 26.7 A GaAs layer per nonzero substrate-strain cell.

Two local curves are reported:

* production: audited Waasmaier/Henke/Stevenson constants;
* constants-matched: the same solver with GID_sl's X0h susceptibilities.

The latter isolates the numerical dynamical-diffraction implementation from
the scattering-database choice.  No instrument convolution is applied.
"""

from __future__ import annotations

import json
from pathlib import Path

import matplotlib

matplotlib.use("Agg")
import matplotlib.pyplot as plt
import numpy as np

from xrd_strain.crystals.gaas_004_10kev_300k import (
    gaas_004_10kev_300k_constants,
)
from xrd_strain.crystals.gaas_004_dynamical import (
    xrd_slab_gaas_lowmem_with_constants,
)

REPO = Path(__file__).resolve().parents[1]
DATA = REPO / "tests" / "data"
STRAIN_FILE = DATA / "fig3_dalembert_substrate_strain.npz"
GID_FILE = DATA / "gid_sl_fig3_dalembert.dat"
REPORT = REPO / "docs" / "fig3_gid_sl_benchmark.json"
FIGURE = REPO / "docs" / "images" / "fig3_gid_sl_benchmark.png"

GID_CHI_0 = -1.8040e-5 + 1j * 3.6087e-7
GID_CHI_H = -1.0360e-5 + 1j * 3.3717e-7
STRAIN_EPS = 1e-6


def factors_from_susceptibilities(chi_0: complex, chi_h: complex) -> np.ndarray:
    """Convert standard susceptibilities to this solver's structure factors."""
    c = gaas_004_10kev_300k_constants()
    scale = (
        -c.classical_electron_radius_angstrom
        * c.wavelength_angstrom**2
        / (np.pi * c.lattice_angstrom**3)
    )

    def factor(chi: complex) -> complex:
        return chi.real / scale + 1j * (-chi.imag / scale)

    return np.array([factor(chi_0), factor(chi_h)], dtype=np.complex128)


def calculate(scan_arcsec: np.ndarray, strain: np.ndarray, factors: np.ndarray):
    c = gaas_004_10kev_300k_constants()
    theta_b = np.degrees(
        np.arcsin(c.wavelength_angstrom / (2.0 * (c.lattice_angstrom / 4.0)))
    )
    theta = theta_b + scan_arcsec / 3600.0
    with np.load(STRAIN_FILE, allow_pickle=False) as data:
        dz_angstrom = float(data["dz_angstrom"])
    return xrd_slab_gaas_lowmem_with_constants(
        theta,
        strain,
        dz_angstrom,
        STRAIN_EPS,
        a_gaas=c.lattice_angstrom,
        f_gaas=factors,
        re=c.classical_electron_radius_angstrom,
        wavelength=c.wavelength_angstrom,
    )


def metrics(scan: np.ndarray, reference: np.ndarray, curve: np.ndarray) -> dict:
    mask = reference > 1e-6
    dlog = np.log10(curve[mask]) - np.log10(reference[mask])
    i_ref = int(np.argmax(reference))
    i_curve = int(np.argmax(curve))
    return {
        "log10_rms_diff": float(np.sqrt(np.mean(dlog**2))),
        "log10_abs_diff_p95": float(np.percentile(np.abs(dlog), 95)),
        "log10_max_abs_diff": float(np.max(np.abs(dlog))),
        "log10_correlation": float(
            np.corrcoef(np.log10(reference[mask]), np.log10(curve[mask]))[0, 1]
        ),
        "peak_arcsec": float(scan[i_curve]),
        "peak_reflectivity": float(curve[i_curve]),
        "reference_peak_arcsec": float(scan[i_ref]),
        "reference_peak_reflectivity": float(reference[i_ref]),
        "centroid_arcsec": float(np.sum(scan * curve) / np.sum(curve)),
    }


def main() -> int:
    with np.load(STRAIN_FILE, allow_pickle=False) as data:
        strain = data["substrate_strain"]
        z_nm = np.arange(len(strain)) * float(data["dz_angstrom"]) / 10.0
        source = {
            "model": str(data["model"]),
            "preset": str(data["preset"]),
            "t_max_s": float(data["t_max_s"]),
            "dz_angstrom": float(data["dz_angstrom"]),
            "n_cells": int(len(strain)),
        }

    scan, gid = np.loadtxt(GID_FILE).T
    constants = gaas_004_10kev_300k_constants()
    production = calculate(scan, strain, constants.solver_factors)
    matched = calculate(
        scan, strain, factors_from_susceptibilities(GID_CHI_0, GID_CHI_H)
    )

    production_metrics = metrics(scan, gid, production)
    matched_metrics = metrics(scan, gid, matched)
    passed = bool(
        production_metrics["log10_correlation"] > 0.995
        and abs(
            production_metrics["peak_arcsec"]
            - production_metrics["reference_peak_arcsec"]
        )
        <= float(scan[1] - scan[0])
        and matched_metrics["log10_rms_diff"] < 0.025
        and matched_metrics["log10_correlation"] > 0.999
    )
    report = {
        "reference": (
            "Stepanov GID_sl, X0h database, GaAs 004, 10 keV, sigma, "
            "symmetric Bragg, instrument=none; queried 2026-07-18"
        ),
        "strain_source": source,
        "gid_sl_susceptibilities": {
            "chi_0": [GID_CHI_0.real, GID_CHI_0.imag],
            "chi_h": [GID_CHI_H.real, GID_CHI_H.imag],
        },
        "production_constants": production_metrics,
        "gid_constants_matched": matched_metrics,
        "passed": passed,
    }
    REPORT.write_text(json.dumps(report, indent=2) + "\n")

    fig, axes = plt.subplots(2, 2, figsize=(12, 7))
    ax = axes[0, 0]
    ax.semilogy(scan, gid, "k-", lw=1.5, label="GID_sl (X0h database)")
    ax.semilogy(scan, production, "r--", lw=1.0, label="production constants")
    ax.semilogy(scan, matched, color="tab:blue", ls=":", lw=1.2, label="GID constants matched")
    ax.set_ylabel("reflectivity")
    ax.set_title("full rocking curve")
    ax.legend(fontsize=8)
    ax.grid(alpha=0.3)

    ax = axes[0, 1]
    near = (scan >= -15) & (scan <= 15)
    ax.plot(scan[near], gid[near], "k-", lw=1.5, label="GID_sl")
    ax.plot(scan[near], production[near], "r--", lw=1.0, label="production")
    ax.plot(scan[near], matched[near], color="tab:blue", ls=":", lw=1.2, label="matched")
    ax.set_title("substrate Darwin peak")
    ax.set_ylabel("reflectivity")
    ax.grid(alpha=0.3)

    ax = axes[1, 0]
    mask = gid > 1e-6
    ax.plot(
        scan[mask],
        np.log10(production[mask]) - np.log10(gid[mask]),
        "r-",
        lw=0.8,
        label="production constants",
    )
    ax.plot(
        scan[mask],
        np.log10(matched[mask]) - np.log10(gid[mask]),
        color="tab:blue",
        lw=0.8,
        label="GID constants matched",
    )
    ax.axhline(0, color="k", lw=0.5)
    ax.set_xlabel(r"$\theta-\theta_B$ (arcsec)")
    ax.set_ylabel(r"$\Delta\log_{10}R$")
    ax.set_title("pointwise residual")
    ax.legend(fontsize=8)
    ax.grid(alpha=0.3)

    ax = axes[1, 1]
    ax.plot(z_nm, strain * 1e3, "k-", lw=0.8)
    ax.set_xlim(0, 9000)
    ax.set_xlabel("depth in GaAs (nm)")
    ax.set_ylabel(r"strain $\times 10^3$")
    ax.set_title("d'Alembert substrate strain, 1.8 ns")
    ax.grid(alpha=0.3)

    fig.suptitle(
        "Sci. Rep. Fig. 3 strain: production XRD vs GID_sl\n"
        "GaAs (004), 10 keV, sigma, no instrument convolution",
        fontsize=11,
    )
    fig.tight_layout()
    fig.savefig(FIGURE, dpi=150)
    plt.close(fig)

    print(
        f"production: RMS={production_metrics['log10_rms_diff']:.4f}, "
        f"corr={production_metrics['log10_correlation']:.6f}"
    )
    print(
        f"GID constants matched: RMS={matched_metrics['log10_rms_diff']:.4f}, "
        f"corr={matched_metrics['log10_correlation']:.6f}"
    )
    print(f"{'PASS' if passed else 'FAIL'}; report: {REPORT}; figure: {FIGURE}")
    return 0 if passed else 1


if __name__ == "__main__":
    raise SystemExit(main())
